'use strict';


Math.rand = Math.rand || function(maxVal){
  if(maxVal !== 0 && !maxVal){
    maxVal = 1;
  }
  let val = Math.random() * maxVal; // Rnd() * maxVal
  return val;
};


class miscMath{
  //*** fRand ********************************************************************
  //* mimics the C frand fucntion
  //******************************************************************************
  static fRand(maxVal){
    console.debug("Deprecated: miscMath.fRand");
    Math.rand(maxVal);
  }
  //*** fRand ********************************************************************
  
  
  
  //*** sq ***********************************************************************
  //* Calculates the square of value.
  //******************************************************************************
  static sq(x){
    return Math.pow(x,2);
  }
  //*** sq ***********************************************************************
  
  
  
  //*** sqrt *********************************************************************
  //*
  //******************************************************************************
  static sqrt(x){
    let root = Math.pow(x,0.5);
    return root;
  }
  //*** sqrt *********************************************************************
  
  
  
  //*** atan *********************************************************************
  //*
  //******************************************************************************
  static atan(y,x){
    return Math.atan(y,x);
  }
  //*** atan *********************************************************************
  
  
  
  //*** WrapByte *****************************************************************
  //*
  //******************************************************************************
  static WrapByte(b){
    if (b < 0){
      return this.WrapByte(256 - b);
    }
    else{
      return b % 256;
    }
  }
  //*** WrapByte *****************************************************************
  
  
}
